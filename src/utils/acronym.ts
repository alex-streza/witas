const lowercase = (word: string) => word.toLowerCase();

// Dictionary of words
// This is indexed by first letter.
// dictionary.a = [ 'apricots', 'always', ...]
const dictionary: {
  [key: string]: string[];
} = {};

// Helper Functions
const getWord = (letter: string) =>
  dictionary[letter][Math.floor(Math.random() * dictionary[letter].length)];

// Set up the dictionary of words using word-list
function initialize() {
  const alphabet = "abcdefghijklmnopqrstuvwxyz";

  // Empty array for each letter
  for (let i = 0; i < alphabet.length; i++) dictionary[alphabet.charAt(i)] = [];

  // Put all the words into the right spot.
  for (let i = 0; i < words.length; i++)
    dictionary[words[i].charAt(0)].push(words[i]);
}

// Main function
export function acronym(text: string) {
  // for each word find a random word that starts with the same letter
  // and replace it with that word.

  const acr = text
    .split("")
    .map((word) => {
      const letter = lowercase(word);
      return getWord(letter);
    })
    .join(" ");

  return acr;
}

initialize();
